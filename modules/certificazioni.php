                <ul>
                    <li>CLASSE A1, che corrisponde alla qualità più elevata, con un contenuto di ceneri massimo dello 0,7%</li>
                    <li>CLASSE A2, caratterizzata da un contenuto di ceneri ≤1.2</li>
